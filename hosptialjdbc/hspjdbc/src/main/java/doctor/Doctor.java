package doctor;

public class Doctor {
	public int doctorId;
	public String doctorName;
	public String doctorSpecilist;
	public double doctorFees;
	public double doctorSalary;
	
	public Doctor(){}

	public int getDoctorId() {
		return doctorId;
	}

	public void setDoctorId(int doctorId) {
		this.doctorId = doctorId;
	}

	public String getDoctorName() {
		return doctorName;
	}

	public void setDoctorName(String doctorName) {
		this.doctorName = doctorName;
	}

	public String getDoctorSpecilist() {
		return doctorSpecilist;
	}

	public void setDoctorSpecilist(String doctorSpecilist) {
		this.doctorSpecilist = doctorSpecilist;
	}

	public double getDoctorFees() {
		return doctorFees;
	}

	public void setDoctorFees(double doctorFees) {
		this.doctorFees = doctorFees;
	}

	public double getDoctorSalary() {
		return doctorSalary;
	}

	public void setDoctorSalary(double doctorSalary) {
		this.doctorSalary = doctorSalary;
	}
	
	
		
		

}
